#include "3-calc.h"

/**
 * op_add - fn to add two numbers
 * @a: number a
 * @b: number b
 *
 * Return: {int} result of add operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - fn to substract two numbers
 * @a: number a
 * @b: number b
 *
 * Return: {int} result of substract operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - fn to multiply two numbers
 * @a: number a
 * @b: number b
 *
 * Return: {int} result of multiplication operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - fn to divide two numbers
 * @a: number a
 * @b: number b
 *
 * Return: {int} result of divide operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - fn to get module two numbers
 * @a: number a
 * @b: number b
 *
 * Return: {int} result of modulas operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
